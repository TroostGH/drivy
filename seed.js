// Parser di una riga CSV con campi quotati
function parseCsvLine(line) {
  const out = [];
  let cur = '';
  let inQ = false;
  for (let i = 0; i < line.length; i++) {
    const c = line[i];
    if (inQ) {
      if (c === '"') {
        if (line[i + 1] === '"') { cur += '"'; i++; }
        else inQ = false;
      } else cur += c;
    } else {
      if (c === '"') inQ = true;
      else if (c === ',') { out.push(cur); cur = ''; }
      else cur += c;
    }
  }
  out.push(cur);
  return out;
}

const num = (s) => {
  if (s == null) return 0;
  const v = parseFloat(String(s).replace(',', '.'));
  return Number.isFinite(v) ? v : 0;
};

// "2026-05-20 16:41:21" -> ISO
const toIso = (s) => (s || '').trim().replace(' ', 'T');

// Converte la stringa CSV Drivvo in record Drivy per un dato vehicleId
export function parseDrivvo(csv, vehicleId) {
  const lines = csv.split(/\r?\n/);
  const entries = [];
  let section = null;
  let seenHeader = false;
  let counter = 0;
  const mkId = (d) => `seed_${vehicleId}_${(d || '').replace(/[^0-9]/g, '')}_${counter++}`;

  for (const raw of lines) {
    const line = raw.trim();
    if (!line) continue;
    if (line.startsWith('##')) {
      section = line.slice(2).trim().toLowerCase();
      seenHeader = false;
      continue;
    }
    // salta l'header di ciascuna sezione
    if (!seenHeader) { seenHeader = true; continue; }

    const f = parseCsvLine(line);

    if (section === 'refuelling') {
      const odo = num(f[0]);
      const date = toIso(f[1]);
      entries.push({
        id: mkId(f[1]),
        vehicleId,
        type: 'fuel',
        date,
        odometer: odo,
        cost: num(f[4]),
        liters: num(f[5]),
        pricePerLiter: num(f[3]),
        fullTank: (f[6] || '').trim() === 'Sì',
        fuelType: (f[2] || 'Diesel').trim(),
        station: (f[19] || '').trim(),
        note: (f[23] || '').trim(),
      });
    } else if (section === 'expense') {
      const dtype = (f[3] || '').trim();
      const note = (f[8] || '').trim();
      entries.push({
        id: mkId(f[1]),
        vehicleId,
        type: 'maintenance',
        date: toIso(f[1]),
        odometer: num(f[0]),
        cost: num(f[2]),
        subtype: SUBTYPE_MAP[dtype] || dtype || 'Altro',
        note,
      });
    } else if (section === 'service') {
      const dtype = (f[3] || '').trim();
      const note = (f[7] || '').trim();
      entries.push({
        id: mkId(f[1]),
        vehicleId,
        type: 'maintenance',
        date: toIso(f[1]),
        odometer: num(f[0]),
        cost: num(f[2]),
        subtype: SUBTYPE_MAP[dtype] || dtype || 'Altro',
        note,
      });
    }
  }
  // ordina dal più recente al più vecchio
  entries.sort((a, b) => (a.date < b.date ? 1 : -1));
  return entries;
}

